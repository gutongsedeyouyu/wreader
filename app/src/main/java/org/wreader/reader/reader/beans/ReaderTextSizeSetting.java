package org.wreader.reader.reader.beans;

public class ReaderTextSizeSetting {
    public final int textSizeSmall;

    public final int textSizeDefault;

    public final int textSizeLarge;

    public ReaderTextSizeSetting(int textSizeSmall, int textSizeDefault, int textSizeLarge) {
        this.textSizeSmall = textSizeSmall;
        this.textSizeDefault = textSizeDefault;
        this.textSizeLarge = textSizeLarge;
    }
}
