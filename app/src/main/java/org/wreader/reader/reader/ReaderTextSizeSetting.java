package org.wreader.reader.reader;

class ReaderTextSizeSetting {
    final int textSizeSmall;

    final int textSizeDefault;

    final int textSizeLarge;

    ReaderTextSizeSetting(int textSizeSmall, int textSizeDefault, int textSizeLarge) {
        this.textSizeSmall = textSizeSmall;
        this.textSizeDefault = textSizeDefault;
        this.textSizeLarge = textSizeLarge;
    }
}
